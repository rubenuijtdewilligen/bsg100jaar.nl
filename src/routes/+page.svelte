<h1 class="text-center text-3xl font-bold">Het feest van de eeuw: 100 jaar BSG!</h1>

<p class="my-2">
  Op <b>6 juni 2026</b> vieren we samen met leden, oud-leden en reünisten
  <b>100 jaar Brabants Studenten Gilde</b>. Een volle dag herinneringen ophalen en nieuwe
  herinneringen maken.
</p>

<p class="my-2">
  Zorg dat JIJ erbij bent in Oirschot, zodat ook jij kunt zeggen: <b>Hier heb ik meer gestaan!</b>
</p>

<h3 class="mt-3 text-xl font-bold underline">Het programma</h3>
<h3 class="text-lg font-bold">10:30 - Officiële opening</h3>
<p class="mb-3">
  We starten in de <b>Kerk van Oirschot</b> met een feestelijke opening, met onder andere
  gedeputeerde Dhr van Gruijthuijsen van Provincie Brabant. Journalist <b>Paul Spapens</b> neemt ons
  op levendige wijze mee naar de vroege jaren van BSG, waarin tijdens de Paaskampen in diverse Brabantse
  dorpen sociografische onderzoeken uitgevoerd zijn. De opening is voor iedereen toegankelijk.
</p>

<h3 class="text-lg font-bold">12.30 - Naar de Beurs & lunch</h3>
<p>
  Met alle BSG leden en reünisten verplaatsen we ons naar <b>De Beurs</b> (op 100 meter), onze uitvalsbasis
  voor de rest van de dag.
</p>
<p class="mb-3">We beginnen met een <b>goed verzorgde lunch</b>: hét moment om bij te praten.</p>

<h3 class="text-lg font-bold">14.00 uur – Middagactiviteiten</h3>
<p class="mb-3">
  De middag bestaat uit twee delen, verveling is uitgesloten. Voor ieder wat wils: van <b
    >rikken tot een stadswandeling</b
  >. Natuurlijk ontbreekt ook het <b>Ruure en Pruuve</b> niet.
</p>
<h3 class="text-lg font-bold">17.00 uur – Borrel & diner</h3>
<p class="mb-3">We luiden de avond in met een gezellige borrel en heerlijk diner.</p>
<h3 class="text-lg font-bold">20.00 – 01.30 uur – Feest!</h3>
<p class="mb-3">
  Het feest barst los in de feestzaal van De Beurs met <b>coverband Hotsho</b> en een <b>DJ</b>. De
  voetjes-van-de-vloer-cie kan weer shinen!
</p>

